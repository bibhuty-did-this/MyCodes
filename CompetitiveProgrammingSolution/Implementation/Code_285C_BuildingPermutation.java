public class Code_285C_BuildingPermutation {
    public static void main(String[] args) {
        InputStream inputStream = System.in;
        OutputStream outputStream = System.out;
        InputReader in = new InputReader(inputStream);
        OutputWriter out = new OutputWriter(outputStream);
        ProblemSolver solver = new ProblemSolver();
        solver.solve(1, in, out);
        out.close();
    }

    static class ProblemSolver {
        public void solve(int testNumber, InputReader in, OutputWriter out) {
            int n = in.nextInt();
            boolean[] permutation = new boolean[n + 1];
            Arrays.fill(permutation, false);
            ArrayList<Integer> numbers = new ArrayList<>();
            for (int i = 1; i <= n; ++i) {
                int get = in.nextInt();
                if (1 <= get && get <= n) {
                    if (!permutation[get]) permutation[get] = true;
                    else numbers.add(get);
                } else numbers.add(get);
            }
            int current = 1, limit = numbers.size();
            long total = 0;
            Collections.sort(numbers);
            for (int i = 0; i < limit; ++i) {
                while (permutation[current]) ++current;
                total += Math.abs(current - numbers.get(i));
                permutation[current] = true;
            }
            out.print(total);
        }

    }

    static class OutputWriter {
        private final PrintWriter writer;

        public OutputWriter(OutputStream outputStream) {
            writer = new PrintWriter(new BufferedWriter(new OutputStreamWriter(outputStream)));
        }

        public OutputWriter(Writer writer) {
            this.writer = new PrintWriter(writer);
        }

        public void print(Object... objects) {
            for (int i = 0; i < objects.length; i++) {
                if (i != 0) {
                    writer.print(' ');
                }
                writer.print(objects[i]);
            }
        }

        public void close() {
            writer.close();
        }

    }

    static class InputReader {
        private InputStream stream;
        private byte[] buf = new byte[1024];
        private int curChar;
        private int numChars;
        private InputReader.SpaceCharFilter filter;

        public InputReader(InputStream stream) {
            this.stream = stream;
        }

        public int read() {
            if (numChars == -1) throw new InputMismatchException();
            if (curChar >= numChars) {
                curChar = 0;
                try {
                    numChars = stream.read(buf);
                } catch (IOException e) {
                    throw new InputMismatchException();
                }
                if (numChars <= 0) return -1;
            }
            return buf[curChar++];
        }

        public int nextInt() {
            int c = read();
            while (isSpaceChar(c)) c = read();
            int sgn = 1;
            if (c == '-') {
                sgn = -1;
                c = read();
            }
            int res = 0;
            do {
                if (c < '0' || c > '9')
                    throw new InputMismatchException();
                res *= 10;
                res += c - '0';
                c = read();
            } while (!isSpaceChar(c));
            return res * sgn;
        }

        public boolean isSpaceChar(int c) {
            if (filter != null)
                return filter.isSpaceChar(c);
            return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
        }

        public interface SpaceCharFilter {
            boolean isSpaceChar(int ch);

        }

    }
}

